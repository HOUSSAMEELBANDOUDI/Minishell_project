#include "../include/libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*old;

	if (!lst || !new)
		return ;
	if (*lst != NULL)
	{
		old = *lst;
		while (old->next)
			old = old->next;
		old->next = new;
	}
	else
		*lst = new;
}
